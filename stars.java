package javaprograms;

public class stars {

	public static void main(String[] args) {
		
	for (int i = 0; i < 5; i++) {
		
		
		
		
		for (int j = 0; j < 5-i; j++) {
			
			System.out.print(" ");
			
		}
		
		for (int k = 0; k < 1+i; k++) {
			System.out.print("*"+" ");
		}
		
		System.out.println();
	}
	
	
	
for (int i = 0; i < 5; i++) {
		
		
		
		
		for (int j = 0; j < 5-i; j++) {
			
			System.out.print(" ");
			
		}
		
		for (int k = 0; k < 1+i; k++) {
			System.out.print(i+k+" ");
		}
		
		
		System.out.println();
	}
	
	}
	
}
